'''
У лінійному масиві знайти максимальний елемент. Вставте порядковий
номер елемента за ним, пересунувши всі залишилися на одну позицію вправо.
Гармідер Анастасія 122В
'''
a=[1,2,3,4,5,6,200,1,2]#масив
d=list()
b=max(a)#шукаємо максимальний елемент
for c in range(len(a)):#прохидомось по масиву
    d.append(a[c])#заносимо до іншого масиву
    if a[c]==b:#якщо умова винолась то ставимо після максимального
        d.append(c)#добаляємо всьо сюди
print(d)